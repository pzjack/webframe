package com.dinglicom.pricepolicy.demain;

/**
 *
 * @author panzhen
 */
public class OrgDealarRespItem {
    private Long id;
    private String name;
    
    public OrgDealarRespItem() {}
    public OrgDealarRespItem(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
}
