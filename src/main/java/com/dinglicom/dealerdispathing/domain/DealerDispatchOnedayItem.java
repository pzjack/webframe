package com.dinglicom.dealerdispathing.domain;

/**
 *
 * @author panzhen
 */
public class DealerDispatchOnedayItem {
    private String id;
    private String station;
    private String manager;
    private String tel;
    private String address;
    
    public DealerDispatchOnedayItem() {}
    public DealerDispatchOnedayItem(String id, String station, String manager, String tel, String address) {
        this.id = id;
        this.station = station;
        this.manager = manager;
        this.tel = tel;
        this.address = address;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the station
     */
    public String getStation() {
        return station;
    }

    /**
     * @param station the station to set
     */
    public void setStation(String station) {
        this.station = station;
    }

    /**
     * @return the manager
     */
    public String getManager() {
        return manager;
    }

    /**
     * @param manager the manager to set
     */
    public void setManager(String manager) {
        this.manager = manager;
    }

    /**
     * @return the tel
     */
    public String getTel() {
        return tel;
    }

    /**
     * @param tel the tel to set
     */
    public void setTel(String tel) {
        this.tel = tel;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }
}
