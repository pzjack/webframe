package com.dinglicom.frame.sys.domain;

/**
 *
 * @author panzhen
 */
public class CityRespItem {

    private long cid;
    private String city;

    public CityRespItem() {
    }

    public CityRespItem(long cid, String city) {
        this.cid = cid;
        this.city = city;
    }

    /**
     * @return the cid
     */
    public long getCid() {
        return cid;
    }

    /**
     * @param cid the cid to set
     */
    public void setCid(long cid) {
        this.cid = cid;
    }

    /**
     * @return the city
     */
    public String getCity() {
        return city;
    }

    /**
     * @param city the city to set
     */
    public void setCity(String city) {
        this.city = city;
    }
}
