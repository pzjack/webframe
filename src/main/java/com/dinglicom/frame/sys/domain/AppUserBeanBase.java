package com.dinglicom.frame.sys.domain;

/**
 *
 * @author panzhen
 */
public class AppUserBeanBase {
    protected long user_id;
    protected String name;
    protected String mobile_phone;
    
    public AppUserBeanBase() {}
    public AppUserBeanBase (long id, String name, String phone) {
        this.user_id = id;
        this.name = name;
        this.mobile_phone = phone;
    }

    /**
     * @return the user_id
     */
    public long getUser_id() {
        return user_id;
    }

    /**
     * @param user_id the user_id to set
     */
    public void setUser_id(long user_id) {
        this.user_id = user_id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the mobile_phone
     */
    public String getMobile_phone() {
        return mobile_phone;
    }

    /**
     * @param mobile_phone the mobile_phone to set
     */
    public void setMobile_phone(String mobile_phone) {
        this.mobile_phone = mobile_phone;
    }
}
